    private void cbMobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbMobilActionPerformed
        // TODO add your handling code here:
        String noPol = (String) cbMobil.getSelectedItem();
        for (Pengembalian p : dataList) {
        if (p.getNoPolisi().equals(noPol)) {
            lblNama.setText(p.nama);
            lblPeminjaman.setText(p.tglPinjam);
            lblPengembalian.setText(p.tglKembali);
            lblDurasi.setText(String.valueOf(p.getLamaPinjam()));
            lblHarga.setText(String.valueOf(p.getTotal()));
        }
    }
    }//GEN-LAST:event_cbMobilActionPerformed

    private void btnKembalikanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKembalikanActionPerformed
            // TODO add your handling code here:
        String noPol = (String) cbMobil.getSelectedItem();
    DefaultTableModel model = (DefaultTableModel) tblMobil.getModel();

    // Cari baris dengan no polisi yg sesuai dan hapus
    for (int i = 0; i < model.getRowCount(); i++) {
        if (model.getValueAt(i, 0).equals(noPol)) {
            model.removeRow(i);
            break; // keluar setelah hapus
        }
    }

    // Hapus dari dataList juga kalau mau bersih
    dataList.removeIf(p -> p.getNoPolisi().equals(noPol));

    // Clear label
    lblNama.setText("-");
    lblPeminjaman.setText("-");
    lblPengembalian.setText("-");
    lblDurasi.setText("-");
    lblHarga.setText("-");

    // Show notif
    javax.swing.JOptionPane.showMessageDialog(this, "Mobil berhasil dikembalikan!");
    }//GEN-LAST:event_btnKembalikanActionPerformed

