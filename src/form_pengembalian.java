    private void tampilkanTabel() {
    // Bikin model dengan kolom dan cell non-editable
    DefaultTableModel model = new DefaultTableModel(
        new Object[] { "No Polisi", "Nama Peminjam", "Harga", "Tanggal Peminjaman", 
                       "Tanggal Pengembalian", "Lama Peminjaman", "Total Harga" }, 
        0 
    ) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Semua cell tidak bisa diedit
        }
    };

    // Tambahkan data ke model
    for (Pengembalian p : dataList) {
        model.addRow(new Object[] {
            p.getNoPolisi(),
            p.nama,
            p.getHargaPerHari(),
            p.tglPinjam,
            p.tglKembali,
            p.getLamaPinjam(),
            p.getTotal()
        });
    }

    // Set model ke table
    tblMobil.setModel(model);

    // Atur lebar kolom
    tblMobil.setAutoResizeMode(JTable.AUTO_RESIZE_OFF); // Supaya lebar kolom sesuai preferredWidth
    tblMobil.getColumnModel().getColumn(0).setPreferredWidth(100); // No Polisi
    tblMobil.getColumnModel().getColumn(1).setPreferredWidth(120); // Nama Peminjam
    tblMobil.getColumnModel().getColumn(2).setPreferredWidth(80);  // Harga
    tblMobil.getColumnModel().getColumn(3).setPreferredWidth(120); // Tanggal Peminjaman
    tblMobil.getColumnModel().getColumn(4).setPreferredWidth(135); // Tanggal Pengembalian
    tblMobil.getColumnModel().getColumn(5).setPreferredWidth(115); // Lama Peminjaman
    tblMobil.getColumnModel().getColumn(6).setPreferredWidth(150); // Total Harga

    // Atur tinggi row
    tblMobil.setRowHeight(25);
    }
    
