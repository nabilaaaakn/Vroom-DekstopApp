    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // Tempelkan ini di dalam method
    try {
        String hargaStr = lblHarga.getText().trim();
        String lamaStr = txtLama.getText().trim();

        if (hargaStr.isEmpty() || lamaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harga atau lama pinjam belum diisi!");
            return;
        }

        double harga = Double.parseDouble(hargaStr);
        int lama = Integer.parseInt(lamaStr);

        double total = harga * lama;
        lblTotal.setText("Rp " + total);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid pada harga dan lama pinjam.");
    }
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        DefaultTableModel model = (DefaultTableModel) table_transaksi.getModel();

    String nama = lblNama.getText();
    String nopol = cbMobil.getSelectedItem().toString();
    String harga = lblHarga.getText();
    String tglPinjam = new SimpleDateFormat("dd-MM-yyyy").format(dtPeminjaman.getDate());
    String tglKembali = new SimpleDateFormat("dd-MM-yyyy").format(dtPengembalian.getDate());
    String lama = txtLama.getText();
    String total = lblTotal.getText();

    model.addRow(new Object[]{nama, nopol, harga, tglPinjam, tglKembali, lama, total});        // TODO add your handling code here:_
    JOptionPane.showMessageDialog(this,
    "Data transaksi berhasil disimpan!",
    "Sukses",
    JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnSubmitActionPerformed

