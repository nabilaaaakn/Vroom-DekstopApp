    // Menarik data menggunakan Get 
    private void hitungLamaPinjam() {
    try {
        Date tglPinjam = dtPeminjaman.getDate();
        Date tglKembali = dtPengembalian.getDate();

        if (tglPinjam != null && tglKembali != null) {
            long diff = tglKembali.getTime() - tglPinjam.getTime();
            long lama = TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS);
            txtLama.setText(String.valueOf(lama));
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
  }
   

